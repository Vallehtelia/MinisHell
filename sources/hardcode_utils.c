#include "../includes/minishell.h"

int	handle_export(t_minishell *mshell)
{
	int	i;
	int	l;

	i = 0;
	while (mshell->cmds[i])
	{
		l = 0;
		while (mshell->cmds[i]->cmd[l])
		{
			if (ft_strncmp(mshell->cmds[i]->cmd[l], "export", 7) == 0)
			{
				if (mshell->num_of_cmds == 1)
				{
					export_env(mshell, mshell->cmds[i]->cmd);
					return (1);
				}
				else
					return (0);
			}
			l++;
		}
		i++;
	}
	return (0);
}

